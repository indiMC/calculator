#first no
fn = float(input("Enter your first number"))
#second no
sn = float(input("Enter your sencond number"))
#operator eg:+,-,*,/.
op = input("Enter the operator you want")

if (op == '+'):
    print(fn + sn)
elif (op == '-'):
    print(fn - sn)
elif (op == '*'):
    print(fn * sn)
elif (op == '/'):
    print(fn / sn)
